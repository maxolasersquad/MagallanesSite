<h2><a name="builtin-symfony2"></a>Symfony 2</h2>
<div>
    <p>
    The following tasks are available for interacting with the <strong>Symfony 2</strong> console:
    <ul>
        <li><strong>symfony2/assetic-dump</strong> - Dumps all Assetics.</li>
        <li><strong>symfony2/assets-install</strong> - Install the assets. Optional parameters: <em>target</em>, <em>symlink</em>, and <em>relative</em>.</li>
        <li><strong>symfony2/cache-clear</strong> - Clears the App Cache.</li>
        <li><strong>symfony2/cache-warmup</strong> - Warms up the App Cache.</li>
    </ul>

    In all cases the <em>env</em> option is available to specify the app environment. By default it is set to <em>dev</em>.
    </p>
</div>
